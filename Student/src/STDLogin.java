        // TODO add your handling code here:
        try {
            String email = jTextField1.getText();
            char[] pass = jPasswordField1.getPassword();
            String password = new String(pass);
            st = conn.prepareStatement("SELECT * FROM student WHERE std_email=? AND std_password=?");
            st.setString(1, email);
            st.setString(2, password);
            rs = st.executeQuery();
            if(rs.next()){
                STDLogin.OperastdBar op = new STDLogin.OperastdBar(jProgressBar1, email);
                op.start();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Invalid Email or Password, Please Try again.");
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Exception : " + ex.getMessage());
        } finally {
            try {
                st.close();
                rs.close();
                //                    ps.close();
            }catch(SQLException ex) {
                JOptionPane.showMessageDialog(null,"Exception : " + ex.getMessage());
            }
        }
