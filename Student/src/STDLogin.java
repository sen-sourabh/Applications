    class OperastdBar extends Thread
    {
        JProgressBar jpb;
        String email;
        public OperastdBar(JProgressBar jpb, String username)
        {
            this.jpb = jpb;
            this.email = username;
        }
        public void run(){
            try
            {
                TimeUnit.SECONDS.sleep(1);
                jpb.setStringPainted(true);
                for(int i=jpb.getMinimum();i<jpb.getMaximum();i++)
                {
                    jpb.setValue(i);
                    Thread.sleep(5);
                }
                STDHome std_home = new STDHome(email);
                std_home.setVisible(true);
                dispose();
            }
            catch(InterruptedException ex)
            {
                JOptionPane.showMessageDialog(null,"Exception : " + ex.getMessage());
            }
        }
    }
