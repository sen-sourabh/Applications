    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String stdid = null, password = null, name=null, category=null, task="pchanged";
        jLabel2.setVisible(true);
        char[] old_pass = jPasswordField1.getPassword();
        String oldpass = new String(old_pass);
        char[] new_pass = jPasswordField2.getPassword();
        String newpass = new String(new_pass);
        char[] renew_pass = jPasswordField3.getPassword();
        String renewpass = new String(renew_pass);
        try {
            ps = conn.prepareStatement("SELECT std_stdid, std_name, std_category, std_password FROM student WHERE std_email=? AND std_password=? LIMIT 1");
            ps.setString(1, useremail);
            ps.setString(2, oldpass);
            rs = ps.executeQuery();
            while(rs.next()){
                stdid = rs.getString(1);
                name = rs.getString(2);
                category = rs.getString(3);
                password = rs.getString(4);
            }
            ps.close();
            rs.close();
            if(oldpass.equals(password)){
                if(newpass.equals(renewpass)) {
                    st = conn.prepareStatement("UPDATE student SET std_password=? WHERE std_stdid=?");
                    st.setString(1, newpass);
                    st.setString(2, stdid);
                    st.execute();
                    if(SendEmail.sendMail(name, useremail, newpass, category, task)) {
                        JOptionPane.showMessageDialog(null, "Your password has been changed successfully, Check your email.");
                    }else {
                        JOptionPane.showMessageDialog(null, "Your password has been changed successfully, But sending mail has some problems.");
                    }
                    jPasswordField1.setText(" ");
                    jPasswordField2.setText(" ");
                    jPasswordField3.setText(" ");
                    st.close();
                    dispose();
                    STDHome std_home = new STDHome(useremail);
                    std_home.setVisible(true);
                }else {
                    JOptionPane.showMessageDialog(this,"Re-type Password and New Password does not Matched, Please Try again.");
                }
            }else {
                JOptionPane.showMessageDialog(this,"Old Password is Invalid, Please Try again.");
            }
        }catch(HeadlessException | SQLException | MessagingException ex) {
            JOptionPane.showMessageDialog(null,"Exception : " + ex.getMessage());
        } finally {
            jLabel2.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

